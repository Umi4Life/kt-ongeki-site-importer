import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const OUT_FILE = path.join(
	ROOT,
	"src/ongeki-importer/config/tachi-chart-lookups.ts",
);

const CHARTS_URL =
	"https://raw.githubusercontent.com/zkldi/Tachi/main/db/seeds/charts-ongeki.json";
const SONGS_URL =
	"https://raw.githubusercontent.com/zkldi/Tachi/main/db/seeds/songs-ongeki.json";

async function fetchJson(url) {
	const response = await fetch(url);
	if (!response.ok) {
		throw new Error(`Failed to fetch ${url}: ${response.status}`);
	}
	return response.json();
}

export function buildLookups(charts, songs) {
	const songById = Object.fromEntries(songs.map((song) => [song.id, song]));
	const remasterByTitle = {};
	const remasterSongTitleOnly = [];
	const lunaticByTitle = {};

	for (const chart of charts) {
		const difficulty = chart.difficulty;
		if (difficulty !== "Re:MASTER" && difficulty !== "LUNATIC") {
			continue;
		}

		const title = songById[chart.songID]?.title;
		if (!title) {
			continue;
		}

		const inGameID = chart.data?.inGameID;

		if (difficulty === "Re:MASTER") {
			if (inGameID == null) {
				remasterSongTitleOnly.push(title);
				continue;
			}
			remasterByTitle[title] = String(inGameID);
			continue;
		}

		if (inGameID == null) {
			continue;
		}

		lunaticByTitle[title] = String(inGameID);
	}

	remasterSongTitleOnly.sort((a, b) => a.localeCompare(b));

	return { remasterByTitle, remasterSongTitleOnly, lunaticByTitle };
}

function serializeRecord(name, record) {
	const entries = Object.entries(record).sort(([a], [b]) => a.localeCompare(b));
	const lines = entries.map(
		([title, id]) => `\t${JSON.stringify(title)}: ${JSON.stringify(id)},`,
	);
	return `export const ${name}: Record<string, string> = {\n${lines.join("\n")}\n};`;
}

function serializeStringArray(name, values) {
	const lines = values.map((value) => `\t${JSON.stringify(value)},`);
	return `export const ${name}: readonly string[] = [\n${lines.join("\n")}\n];`;
}

export function serializeLookupBody({
	remasterByTitle,
	remasterSongTitleOnly,
	lunaticByTitle,
}) {
	return `${serializeRecord("REMASTER_BY_TITLE", remasterByTitle)}

${serializeStringArray("REMASTER_SONG_TITLE_ONLY", remasterSongTitleOnly)}

${serializeRecord("LUNATIC_BY_TITLE", lunaticByTitle)}
`;
}

function lookupBodyFromFile(contents) {
	const marker = "export const REMASTER_BY_TITLE";
	const index = contents.indexOf(marker);
	return index >= 0 ? contents.slice(index) : "";
}

export function emitTypeScript(lookups) {
	const generatedAt = new Date().toISOString();
	return `// Generated by scripts/generate-tachi-lookups.js — do not edit manually.
// Source: zkldi/Tachi db/seeds/charts-ongeki.json + songs-ongeki.json
// Generated at: ${generatedAt}

${serializeLookupBody(lookups)}`;
}

export function writeLookupsIfChanged(lookups, outFile = OUT_FILE) {
	const nextBody = serializeLookupBody(lookups);
	const existingBody = fs.existsSync(outFile)
		? lookupBodyFromFile(fs.readFileSync(outFile, "utf8"))
		: "";

	if (existingBody === nextBody) {
		return false;
	}

	fs.mkdirSync(path.dirname(outFile), { recursive: true });
	fs.writeFileSync(outFile, emitTypeScript(lookups), "utf8");
	return true;
}

function formatLookupSummary(lookups) {
	return `${Object.keys(lookups.remasterByTitle).length} Re:MASTER, ${lookups.remasterSongTitleOnly.length} Re:MASTER songTitle-only, ${Object.keys(lookups.lunaticByTitle).length} LUNATIC`;
}

export async function main() {
	const [charts, songs] = await Promise.all([
		fetchJson(CHARTS_URL),
		fetchJson(SONGS_URL),
	]);

	const lookups = buildLookups(charts, songs);
	const wrote = writeLookupsIfChanged(lookups);

	if (wrote) {
		console.log(`Wrote ${OUT_FILE} (${formatLookupSummary(lookups)})`);
	} else {
		console.log(`Lookups unchanged (${formatLookupSummary(lookups)})`);
	}
}

const isCli = process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);

if (isCli) {
	main().catch((error) => {
		console.error(error);
		process.exit(1);
	});
}
